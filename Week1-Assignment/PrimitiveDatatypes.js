//Primitive Datatypes
let firstName = "Ekta";
console.log(firstName);
console.log ("Datatype of firstname is: " + typeof firstName); //string

let companyName = null;
console.log(companyName);
console.log ("Datatype of companyName is: " + typeof companyName); //object

let mobileNumber = 999999999;
console.log(mobileNumber);
console.log ("Datatype of mobileNumber is: " + typeof mobileNumber); //number

let isAutomation = true;
console.log(isAutomation);
console.log ("Datatype of isAutomation is: " + typeof isAutomation); //boolean

let hasPlaywright;
console.log(hasPlaywright);
console.log ("Datatype of hasPlaywright is: " + typeof hasPlaywright); //undefined